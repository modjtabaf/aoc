uint32_t part1(const Map& map)
{
    // std:: cout << "249" << std::endl;
    int nx = map.front().size();
    int ny = map.size();

    // std:: cout << "253" << std::endl;
    AllScores all_scores;
    all_scores.reserve(4);
    // std:: cout << "255" << std::endl;
    for (int k = 0; k < 4; k++)
    {
        auto& map_scores = all_scores.emplace_back();

        map_scores.reserve(ny);
        for (int y=0; y < ny; y++)
        {
            auto& row = map_scores.emplace_back();
            row.reserve(nx);
            for (int x=0; x < nx; x++)
            {
                map[y][x] == '#' ?
                    row.emplace_back(std::make_pair(invalid_score, 0)) :
                    row.emplace_back(std::make_pair(unknown_score, std::numeric_limits<int>::max()));
            }
        }
    }
    // std:: cout << "273" << std::endl;

    int max_steps = 2;
    for (const auto& row: map)
        for (auto c: row)
            if (c == '.') max_steps++;
    // std:: cout << "max steps = " << max_steps << std::endl;

    // find the start and end positions
    int x_start, y_start;
    int x_end, y_end;
    for (int y = 0; y < ny; y++)
    {
        for (int x = 0; x < nx; x++)
        {
            if (map[y][x] == 'S')
            {
                x_start = x;
                y_start = y;
            }
            else if (map[y][x] == 'E')
            {
                x_end = x;
                y_end = y;
            }
        }
    }
    // std::cout << "Initial\n";
    // draw_map(map);
    // std:: cout << __LINE__ << ":" << x_end << ", " << y_end << std::endl;

    for (auto& scores: all_scores)
    {
        scores[y_end][x_end].first = 0;
        scores[y_end][x_end].second = 0;
    }

    // map[y_start][x_start] = '.';
    // x_start = 13;
    // y_start = 13;
    auto ret = try_move(map, all_scores, x_start, y_start, 3, nx, ny, max_steps);

    // std::cout << all_scores[3][2][13].first << ":" << all_scores[3][2][13].second << std::endl; // 1
    // std::cout << all_scores[0][13][13].first << std::endl; // 
    // std::cout << all_scores[1][13][11].first << std::endl;
    // std::cout << all_scores[0][13][10].first << std::endl;
    // std::cout << all_scores[0][7][11].first << std::endl;
    return ret.first;
}

