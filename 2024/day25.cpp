int part1(const Patterns& patterns)
{
    Patterns keys, locks;
    keys.reserve(patterns.size());
    locks.reserve(patterns.size());
    for (const auto& pat: patterns)
    {
        pat[0] == 0b11111 ? locks.emplace_back(pat) : keys.emplace_back(pat);
    }

    int ret{0};
    for (const auto& lock: locks)
    {
        for (const auto& key: keys)
        {
            bool overlap{false};
            auto row1 = lock.begin();
            for (auto row2: key)
            {
                overlap = *(row1++) & row2;
                if (overlap) break;
            }
            if (!overlap) ret++;
        }
    }

    return ret;
}

